public class LoginActivity extends AppCompatActivity {

    EditText etEmail, etPassword;
    Button btnLogin;
    String result;

    TextView tvres;


    @SuppressLint("MissingInflatedId")
    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_login);

        etEmail = findViewById(R.id.etEmail);
        etPassword = findViewById(R.id.etPassword);
        btnLogin = findViewById(R.id.btnLogin);


        btnLogin.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                performLogin(etEmail.getText().toString(), etPassword.getText().toString());
            }
        });
    }

    private void performLogin(final String email, final String password) {
        String url = "https://auctioned.000webhostapp.com/loginjson.php"; // URL of your PHP login script
        RequestQueue queue = Volley.newRequestQueue(this);
        StringRequest postRequest = new StringRequest(Request.Method.POST, url,
                new Response.Listener<String>() {
                    @Override
                    public void onResponse(String response) {

                        result = response;
                        Intent xmlintent = new Intent(LoginActivity.this,UserActivity.class);
                        xmlintent.putExtra("xmlresponse",response);
                        startActivity(xmlintent);





                    }
                },
                new Response.ErrorListener() {
                    @Override
                    public void onErrorResponse(VolleyError error) {
                        Toast.makeText(LoginActivity.this, "Login failed: " + error.toString(), Toast.LENGTH_SHORT).show();
                        Log.d("Error is",error.toString());
                    }
                }
        ) {
            @Override
            protected Map<String, String> getParams() {
                Map<String, String> params = new HashMap<>();
                params.put("email", etEmail.getText().toString());
                params.put("password", etPassword.getText().toString());
                return params;
            }
        };
        queue.add(postRequest);
    }
}